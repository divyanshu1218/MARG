const CareerCard: React.FC<CareerCardProps> = ({ career, onClick }) => {
  const [isBookmarked, setIsBookmarked] = useState(career.bookmarked);

  const toggleBookmark = (e: React.MouseEvent) => {
    e.stopPropagation();
    setIsBookmarked(!isBookmarked);
  };

  return (
    <div 
      className="bg-white p-4 rounded-lg border border-gray-200 hover:shadow-xl hover:-translate-y-1.5 transition-all duration-300 cursor-pointer flex justify-between items-start"
      onClick={() => onClick(career)}
    >
      <div>
        <div className="flex items-center space-x-3">
          <div className="w-10 h-10 flex items-center justify-center bg-marg-bg-light rounded-full text-marg-secondary">{getIconForCategory(career.category)}</div>
          <h3 className="font-semibold text-marg-primary">{career.title}</h3>
        </div>
        <p className="text-sm text-marg-text-secondary uppercase mt-2 ml-14">{career.category}</p>
      </div>
      <button onClick={toggleBookmark} className="text-gray-400 hover:text-marg-accent transition-colors p-1" aria-label="Bookmark career">
        <svg xmlns="http://www.w3.org/2000/svg" className="h-6 w-6" fill={isBookmarked ? 'currentColor' : 'none'} viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
          <path strokeLinecap="round" strokeLinejoin="round" d="M5 5a2 2 0 012-2h10a2 2 0 012 2v16l-7-3.5L5 21V5z" />
        </svg>
      </button>
    </div>
  );
};

export default CareerCard;
